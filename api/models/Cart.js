// importation de mongoose qui est dans le fichier
const mongoose = require("mongoose");
// creation d'une nouvelle instance de la fonction Schema de mongoose
const CartSchema = new mongoose.Schema(
  {
    userId: { type: String, required: true },
    products: [
      {
        productId: {
          type: String,
        },
        quantity: {
          type: Number,
          default: 1,
        },
      },
    ],
  },
  { timestamps: true }
);

module.exports = mongoose.model("Cart", CartSchema);
